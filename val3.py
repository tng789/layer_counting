import numpy as np
from sklearn.cluster import KMeans
import matplotlib.pyplot as plt
import matplotlib.font_manager as fm

def count_layers_v2(yolo_coords, min_distance_ratio=0.5):
    """
    修正版：针对“柱子端面”这种物理结构明确的场景
    逻辑：先排序，看相邻框的 Y 距离差，如果差值超过平均高度的一半，认为是分层点
    """
    if not yolo_coords:
        return 0, []

    # 1. 提取 Y 坐标
    # 注意：YOLO坐标是归一化的 (0-1)，但排序逻辑一样
    y_coords = np.array([box[2] for box in yolo_coords])
    sorted_indices = np.argsort(y_coords)
    sorted_ys = y_coords[sorted_indices]

    # 2. 计算相邻框的 Y 距离差
    diffs = np.diff(sorted_ys) # 相邻框的距离差

    if len(diffs) == 0:
        return 1, [0]

    # 估算平均框高 (用 w 或 h 都可以，这里用 w 代表横向尺寸，但主要看 y 方向的分布)
    # 这里我们用 y 方向的平均跨度作为参考阈值
    avg_height = np.mean([box[4] for box in yolo_coords])
    # 设定分层阈值：如果相邻框的距离大于平均高度的 min_distance_ratio 倍，就认为是分层
    # 对于两层结构，通常设置 0.4 - 0.6 之间比较合适
    threshold = avg_height * min_distance_ratio

    # 3. 判断分层
    # 找出所有距离差大于阈值的位置，这些位置就是“层与层之间的边界”
    layer_boundaries = np.where(diffs > threshold)[0]

    # 层数 = 边界数 + 1
    n_layers = len(layer_boundaries) + 1

    # 分配层标签
    labels = np.zeros(len(yolo_coords), dtype=int)
    current_label = 0

    for i in range(len(sorted_indices)):
        labels[sorted_indices[i]] = current_label
        # 如果当前框和下一个框的距离超过了阈值，层数加1
        if i < len(layer_boundaries) and i == layer_boundaries[i]:
            current_label += 1

    return n_layers, labels.tolist()

